#include "ft_printf.h"

int	ft_print_s(char *s, int *len)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (s[i])
	{
		ft_print_c(s[i]);
		++(*len);
		i++;
	}
	return (1);
}
